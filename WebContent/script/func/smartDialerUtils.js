function showAddDiv(){
	$("#content").hide();
	$("#modifyDiv").hide();
	$("#addDiv").show();
}
function hideAddDiv(){
	$("#content").show();
	$("#modifyDiv").hide();
	$("#addDiv").hide();
}
function showModifyDiv(){
	$("#content").hide();
	$("#modifyDiv").show();
	$("#addDiv").hide();
}
function hideModifyDiv(){
	$("#content").show();
	$("#modifyDiv").hide();
	$("#addDiv").hide();
}